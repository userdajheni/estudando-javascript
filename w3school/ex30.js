let numeroPi = Math.PI
console.log(numeroPi) // saída: 3.141592653589793   

let a = Math.E
console.log(a) // saída: 2.718281828459045
console.log(Math.round(a)) // 3

let b = Math.SQRT2
console.log(b) // saída: 1.4142135623730951
console.log(Math.ceil(b)) // saída 2 - arredonda para cima

let c = Math.SQRT1_2
console.log(c) // saída: 0.7071067811865476
console.log(Math.floor(c)) // 0 - arredonda para baixo


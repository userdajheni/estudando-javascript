a = 5
b = 6

// Potênciação
console.log(Math.pow(a,b)) // saída: 15625
console.log(Math.pow(b,a)) // saída: 7776

// raiz quadrada
console.log(Math.sqrt(a)) // saída: 2.23606797749979
console.log(Math.sqrt(b)) // saída: 2.449489742783178
console.log(Math.sqrt(49)) // saída: 7

// valor absoluto
console.log(Math.abs(-45431)) // saída: 45431

// valor minimo
console.log(Math.min(0,150,30,20,-8,-200)) // saída: -200
console.log(Math.max(0, 150, 30, 20, -8, -200)) // saída: 150

// retornar numero aleatorio
console.log(Math.random()) // entre 0 e 1

// logaritmo - Math.E e Math.log() são gêmeos.  
console.log(Math.log(1)) // saída: 0
console.log(Math.log(2)) // saída: 0.6931471805599453 
console.log(Math.log(3)) // saída: 1.0986122886681096
console.log(Math.log2(8)) // logaritmo de base 2 saída: 3
console.log(Math.log10(1000)) // logaritmo de base 10 saída: 3
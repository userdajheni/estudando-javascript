// potenciação: x elevado a y
let a = Math.pow(8,2)
console.log(a) // saída: 64

//  raiz quadrada 
let b = Math.sqrt(49)
console.log(b) // saída: 7

// valor absoluto (positivo)
let c = Math.abs(-4.7)
console.log(c) // saída: 4.7

// encontrar minimo e maximo 
let d = Math.min(0, 150, 30, 20, -8, -200)
console.log(d) // saída: -200

let e = Math.max(0, 150, 30, 20, -8, -200)
console.log(e) // saída: 150

// numero aleatorio entre 0 e 1
let f = Math.random()
console.log(f) // saída: 0.18251721160466383

